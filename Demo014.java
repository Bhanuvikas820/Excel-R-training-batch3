public class Demo014 {
    void Unaryoperations(){
        int a = 8;
        int b= 4;
        System.out.println(++a);
        System.out.println(--b);
        System.out.println(a++);
        System.out.println(b--);
    }

    public static void main(String[] args) {
        Demo014 obj = new Demo014();
        obj.Unaryoperations();
    }

}
   
